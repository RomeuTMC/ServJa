    <a href='https://github.com/RomeuTMC/ServJa'>ServJa no GitHub</a> e consiste
    em um portal onde <b>Prestadores de Serviço</b> podem anunciar seus serviços 
    gratuitamente, e o publico pode ver os prestadores em formato de um guia, ou 
    solicitar um orçamento descrevendo as necessidades, ou colocar cotações 
    publicas para os profissionais enviarem propostas.<br>
    A ideia é ser um sistema completo para aproximar os prestadores de serviço 
    dos consumidores, permitindo uma fácil localização destes conforme a região 
    onde o prestador e o cliente atuam.<br>
